import javax.swing.*;

public class Enquanto1 {

    public static void main(String[] args) {
        
        //Índice
        int indice = 1;

        //Laço
        while(indice <= 10){
            
            //Exibir índice
            System.out.println(indice);

            //Incrementar
            indice++;

        }

    }

    
}

/*
    Incremento e descremento
    ++ incrementa 1
    -- decrementa 1
    +=2 incrementa 2
    -+3 decrementa 3
    *=6 multiplica por 6
    /=5 divide por 5

*/