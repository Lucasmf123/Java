//Englobar códigos
public class OperadoresRelacionais{

    //Iniciar o sistema
    public static void main(String[] args){

	 //MAIOR
	 System.out.println(5 > 4);

	 //MENOR
	 System.out.println(5 < 4);

	 //MAIOR OU IGUAL
	 System.out.println(10 >= 11);

	 //MENOR OU IGUAL
	 System.out.println(10 <= 11);  

	 //IGUAL
	 System.out.println(5 == 5);

	 //DIFERENTE
	 System.out.println(5 != 5);
    
    }

}


