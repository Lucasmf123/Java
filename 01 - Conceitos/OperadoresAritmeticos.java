//Englobar códigos
public class OperadoresAritmeticos{

    //Iniciar o sistema
    public static void main(String[] args){

	/*
	    Ordem aritmética
	    1) Divisão
	    2) Multiplicação
	    3) Subtração
	    4) Soma
	*/

	//Concatenação = Unir informações

	//Somar
	System.out.println("A soma é "+(5+5));

	//Subtrair
	System.out.println("A subtração é "+(50-14));

	//Multiplicar
	System.out.println("A multiplicação é "+(5*6));

	//Dividir
	System.out.println("A divisão é "+(30/6));

	//Média
	System.out.println("A média é "+((7+7+7)/3));

	//Extrair percentual
	System.out.println("10% de 800 é "+(800*0.10));
    }

}










